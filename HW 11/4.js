/* Дане ціле число. З'ясувати, чи є воно простим (простим називається число,
 більше 1, які не мають інших дільників крім 1 і себе).
*/
const n = 17;
function checkNumb(n) {
  if (n > 1) {
    for (let i = 2; i < n; i++) {
      if (n % i == 0) {
        return "Парне число";
      }
    }
    return "Просте число";
  } else {
    return "Число має буди більше 1";
  }
}

console.log(checkNumb(n));
